def write(path: Path, header, rows):
    with path.open("w", encoding="utf-8-sig", newline="") as f:
        w = csv.writer(f)
        w.writerow(header)
        for r in rows:
            w.writerow(r)
    print(f"  ✓ {path.relative_to(ROOT.parent)}（{len(rows)} 列）")


