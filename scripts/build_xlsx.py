REL_DIR.mkdir(parents=True, exist_ok=True)


def write(path: Path, header: list[str], rows: list[list]):
    wb = Workbook()
    ws = wb.active
    ws.title = path.stem.split("_", 1)[-1][:30]
    ws.append(header)
    for r in rows:
        ws.append(r)
    for col_idx, _ in enumerate(header, start=1):
        col_letter = ws.cell(row=1, column=col_idx).column_letter
        max_len = max((len(str(ws.cell(row=r, column=col_idx).value or "")) for r in range(1, ws.max_row + 1)), default=8)
        ws.column_dimensions[col_letter].width = min(max_len * 2 + 2, 60)
    wb.save(path)
    print(f"  ✓ {path.relative_to(ROOT.parent.parent)}")


